/**
 * Book My Stay App
 *
 * This class represents the entry point of the Hotel Booking Management System.
 * It demonstrates how a Java application starts execution and prints
 * a welcome message to the console.
 *
 * The program illustrates basic Java concepts such as:
 * - Class definition
 * - main() method as the application entry point
 * - Console output using System.out.println()
 * - String literals
 * - Linear program execution flow
 *
 * @author Harshan
 * @version 1.0
 */

public class BookMyStayApp {

    /**
     * The main method is the starting point of the Java application.
     * The JVM calls this method when the program is executed.
     *
     * @param args Command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("======================================");
        System.out.println("      Welcome to Book My Stay App     ");
        System.out.println("   Hotel Booking Management System    ");
        System.out.println("            Version 1.0               ");
        System.out.println("======================================");

        // Inform user that application started successfully
        System.out.println("Application started successfully!");

        // Program ends here
    }
}
